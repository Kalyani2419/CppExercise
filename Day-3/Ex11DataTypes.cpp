#include<iostream>
using namespace std;

int main()
{
	int numInteger = 12; //whole number
	cout<<numInteger<<endl;

	float numFloat = 12.22; // floating point number
	cout<<numFloat<<endl;

	double numDouble = 9.89; // floating point number
	cout<<numDouble<<endl;

	char letter = 'A'; //character
	cout<<letter<<endl;

	bool numBoolean = true; //stores boolean values
	cout<<numBoolean<<endl;

	string text = "Hello World"; //stores text
	cout<<text<<endl;

	return 0;
}
