#include<iostream>
using namespace std;

int main()
{
	int numInteger = 10; //whole number
	cout<<numInteger<<endl;

	float numFloat = 2.6; // floating point number
	cout<<numFloat<<endl;

	double numDouble = 5.90; // floating point number
	cout<<numDouble<<endl;

	//scientific numbers e indicate as 10
	float f1 = 35e3;
	double d1 = 12E4;

	cout<<"float = "<<f1<<endl;
	cout<<"double = "<<d1;

	return 0;
}
