#include<iostream>
using namespace std;

int main()
{
	int x = 5, y = 10;
	
	cout<<(x == y)<<endl; // returns 0
	
	cout<<(x != y)<<endl; // returns 1
	
	cout<<(x > y)<<endl; // returns 0
	
	cout<<(x < y)<<endl; // returns 1
	
	cout<<(x >= y)<<endl; // returns 0
	
	cout<<(x <= y)<<endl; // returns 1
	
	return 0;
}
